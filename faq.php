        <details>
            <summary>Can I always have a bridge to replace missing teeth?</summary>
            <br>
            <p>Yes, if you have enough strong teeth with good bone support and the distance between the core teeth is
                not too long. Your dentist will help you decide the best way of replacing the teeth within your
                budget.</p>
        </details>

        <details>
            <summary>Why should I replace missing teeth?</summary>
            <br>
            <p>Your appearance is one reason. Another is that the gap left by missing teeth can mean greater strain on
                the teeth at either side. A gap can also mean your ‘bite’ is affected, because the teeth next to the
                space can lean into the gap and alter the way the upper and lower teeth bite together. This can then
                lead to food getting packed into the gap, which causes both decay and gum disease.</p>
        </details>

        <details>
            <summary>Do I have an implant for each missing tooth?</summary>
            <br>
            <p>No, unless you’re only having a single tooth replaced. Normally, five or six implants are used to replace
                all the teeth in one jaw, as each implant can usually support two teeth. For a few missing teeth, two or
                three implants may be used.</p>
        </details>

        <details>
            <summary>Can I take the teeth out if they are fixed to implants?</summary>
            <br>
            <p>Most artificial teeth attached to implants can only be placed and removed by the dentist. However, if you
                have complete dentures fixed to the implants by bars, then you’ll be able to take them out for
                cleaning.</p>
        </details>

        <details>
            <summary>What types of products does your clinic offer?</summary>
            <br>
            <p>Our clinic offers a wide range of dental products, including custom-made dental crowns, bridges,
                dentures, veneers, and high-quality dental implants. We also provide advanced teeth whitening kits and
                night guards to protect your teeth from grinding.</p>
        </details>

        <details>
            <summary>Are your dental products covered by warranty?</summary>
            <br>
            <p>Yes, many of our dental products, including implants and crowns, come with a warranty. The length and
                terms of the warranty depend on the product and its usage. During your consultation, we’ll provide you
                with full details on warranties and care instructions.</p>
        </details>

        <details>
            <summary>When dental implants aren't an option - What are the limiting factors?</summary>
            <br>
            <p>Dental implants might not be an option due to insufficient bone density or volume to support the implant,
                medical conditions such as uncontrolled diabetes, gum disease, or certain medications that impair
                healing. Additionally, heavy smoking, financial constraints, or lack of overall oral health might limit
                eligibility. A dentist can evaluate and suggest alternative solutions, like bridges or dentures.</p>
        </details>

        <details>
            <summary>Are dental implants safe?</summary>
            <br>
            <p>Yes, dental implants are generally safe and have a high success rate when performed by trained
                professionals. Like any medical procedure, there are risks, such as infection, nerve damage, or implant
                failure. However, with proper planning, modern techniques, and good oral hygiene, dental implants are a
                reliable and long-term solution for replacing missing teeth.</p>
        </details>

        <details>
            <summary>What should someone research before choosing a clinic to go with?</summary>
            <br>
            <p>Before choosing a clinic, research the dentist's qualifications, certifications, and experience with
                dental implants. Check patient reviews and testimonials. Ensure the clinic follows proper hygiene and
                sterilization protocols. It's also wise to ask about implant brands, costs, warranties, and aftercare
                services. Visiting the clinic for an initial consultation can also help gauge the quality of care.</p>
        </details>

        <details>
            <summary>How long do dental implants last?</summary>
            <br>
            <p>Dental implants can last 20-30 years or even a lifetime with proper care. The lifespan depends on factors
                like the quality of the implant, oral hygiene habits, and regular dental check-ups. While the implant
                itself is permanent, the crown attached to it may need replacement after 10-15 years due to normal wear
                and tear.</p>
        </details>
